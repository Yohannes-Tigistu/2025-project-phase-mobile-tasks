abstract class Failures {
  const Failures();
  @override
  List<Object?> get props => [];
}

class ServerFailure extends Failures {
  ServerFailure();

  @override
  List<Object?> get props => [];
}

class CacheFailure extends Failures {
  CacheFailure();

  @override
  List<Object?> get props => [];
  } 

class NetworkFailure extends Failures {
  NetworkFailure();

  @override
  List<Object?> get props => [];
}

  
